# Making a list...........
list = [2, 3, 5, 9, 7, 6, 0, 1,]
print(list)

# Adding a value in list.......
list.append(8)
list.append(4)
print(list)

# Arranging list in accending order..........
list.sort()
print(list)

# Arranging list in decending order...........
list.sort(reverse=True)
print(list)

# Reverseing the list values.............
list.reverse()
print(list)

# Inserting a number in between the list........
list.insert(5,10)
print(list)

# Remopve a value in the list..........
list.remove(9)
print(list)

# Remove a value ob a particuloar index..........
list.pop(6)
