# Making a list...........
list = ["Aniruddh", "Akash", "Rahul", "Suman", "Krish"]
print(list)

# Adding a value in list.......
list.append("Chirag")
list.append("Vishwas")
print(list)

# Arranging list in accending order..........
list.sort()
print(list)

# Arranging list in decending order...........
list.sort(reverse=True)
print(list)

# Reverseing the list values.............
list.reverse()
print(list)